package com.polopoly.util.exception;

public class NoExternalIdSetException extends Exception {

	public NoExternalIdSetException() {
		super();
	}

	public NoExternalIdSetException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public NoExternalIdSetException(String arg0) {
		super(arg0);
	}

	public NoExternalIdSetException(Throwable arg0) {
		super(arg0);
	}

}
