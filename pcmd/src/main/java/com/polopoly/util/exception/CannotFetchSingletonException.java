package com.polopoly.util.exception;

public class CannotFetchSingletonException extends Exception {

	public CannotFetchSingletonException() {
		super();
	}

	public CannotFetchSingletonException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CannotFetchSingletonException(String arg0) {
		super(arg0);
	}

	public CannotFetchSingletonException(Throwable arg0) {
		super(arg0);
	}

}
